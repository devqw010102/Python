### 함수 선언 이후 호출 가능 ###

# 함수 선언
def hello() :
    print('Hello')

# 함수 호출
hello()

# 인수 있는 함수 선언
def add(a, b) :
    print(a + b)
add(10, 20)

# 매개변수 있고, return 있는 함수
def plus(a, b) :
    return a + b
print(plus(10, 20))

